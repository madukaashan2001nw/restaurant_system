    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String bName = jTextField1.getText();

        if (bName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter bank name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                MySQL.iud("INSERT INTO `bank`(`name`) VALUES('" + bName + "')");;

                JOptionPane.showMessageDialog(this, "New bank registered", "Success", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();

            } catch (Exception e) {
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    
    public void loadBanks() {

        try {
            ResultSet rs = MySQL.search("SELECT * FROM bank");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }
